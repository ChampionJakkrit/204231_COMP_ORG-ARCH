TITLE DISPLAY HEX DIGISTS
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER A HEX DIGIT: ", '$'
MSG2 DB "IN DECIMAL IT IS ", '$'
MSG3 DB "DO YOU WANT TO DO IT AGAIN? ", '$'
CR EQU 0DH
LF EQU 0AH
NEWLINE DB CR, LF, '$'
.CODE
MAIN PROC
; initiation DS
	MOV AX, @DATA
	MOV DS, AX
	
REPEAT_INTPUT:
; display MSG1
	MOV AH, 9
	LEA DX, MSG1
	INT 21H
; input
	MOV AH, 1
	INT 21H
	MOV AH, 9
	LEA DX, NEWLINE
	INT 21H
; if input is number
	CMP AL, 'A'
	JL IS_NUMBER
	
; if input is char
	CMP AL, 'A'
	JGE IS_CHAR
	JMP EXIT
	
IS_NUMBER:
; display MSG2
	MOV AH, 9
	LEA DX, MSG2
	INT 21H
	MOV AH, 2
	MOV DL, AL
	INT 21H
	JMP ASK_AGAIN

IS_CHAR:
; clear AH to 0
	MOV AH, 0
	PUSH AX
	MOV AH, 9
	LEA DX, MSG2
	INT 21H
	POP AX
	SUB AX, 55
	CALL OUTDEC
	JMP ASK_AGAIN
	
ASK_AGAIN:
	MOV AH, 9
	LEA DX, NEWLINE
	INT 21H
	LEA DX, MSG3
	INT 21H 
; input
	MOV AH, 1
	INT 21H
; if input != N go REPEAT_INTPUT
	CMP AL, 'N'
	JNE NEWLINE_
; if input = N go EXIT
	CMP AL, 'N'
	JE EXIT
	
NEWLINE_:
	MOV AH, 9
	LEA DX, NEWLINE
	INT 21H
	JMP REPEAT_INTPUT
	
EXIT:

; return to DOS
	MOV AH, 4CH
	INT 21H

MAIN ENDP
	INCLUDE OUTDEC.ASM 
	END MAIN