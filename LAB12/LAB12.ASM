TITLE DISPLAY STRING PROGRAM
.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
MSG DB "ENTER THREE INITIALS: $"
N1 DB "?"
N2 DB "?"
N3 DB "?"
NEWLINE DB CR, LF, "$"
.CODE
MAIN PROC
; initiaton DS
	MOV AX, @DATA
	MOV DS, AX
; display MSG
	LEA DX, MSG
	MOV AH, 9
	INT 21H
; input user
	MOV AH, 1
	INT 21H
	MOV N1, AL
	
	MOV AH, 1
	INT 21H
	MOV N2, AL
	
	MOV AH, 1
	INT 21H
	MOV N3, AL
; display newline
	LEA DX, NEWLINE
	MOV AH, 9
	INT 21H
; display N1 N2 N3
	LEA DX, NEWLINE
	MOV AH, 9
	INT 21H
	MOV AH, 2
	MOV DL, N1
	INT 21H
	
	LEA DX, NEWLINE
	MOV AH, 9
	INT 21H
	LEA DX, NEWLINE
	MOV AH, 9
	INT 21H
	
	MOV AH, 2
	MOV DL, N2
	INT 21H
	LEA DX, NEWLINE
	MOV AH, 9
	INT 21H
	LEA DX, NEWLINE
	MOV AH, 9
	INT 21H
	
	MOV AH, 2
	MOV DL, N3
	INT 21H
	
; return to DOS
	MOV AH, 4CH
	INT 21H
MAIN ENDP
	END MAIN